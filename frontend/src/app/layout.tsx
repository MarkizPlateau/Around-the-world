import type { Metadata } from 'next';
import './../styles/globals.css';
import { Providers } from './providers';
import LayoutWrapper from '@/wrappers/LayoutWrapper/LayoutWrapper';
import { dynaPuff, lato } from '@/styles/fonts';
import { getServerSession } from 'next-auth';
import { authOptions } from './api/auth/[...nextauth]/authOptions';

export const metadata: Metadata = {
  title: 'Around the world',
  description: 'Generated by create next app',
};

// IDEA
// Checking whether the asynchronous RootLayout will not cause problems in the future

export default async function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  const session = await getServerSession(authOptions);

  return (
    <html lang="en">
      <body className={`${dynaPuff.variable} ${lato.variable}`}>
        <Providers session={session}>
          <LayoutWrapper>{children}</LayoutWrapper>
        </Providers>
      </body>
    </html>
  );
}
